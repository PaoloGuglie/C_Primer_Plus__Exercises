// Preprocessor instructions
#include <stdio.h>

// First function called is always "main()"
int main(void)
{
  // Declaration statement
  int num;

  // Assignment statement
  num = 1;

  // Function calls
  printf("I am a simple ");
  printf("computer.\n");
  printf("My favorite number is %d because it is first.\n", num);

  // Return statement
  return 0;
}